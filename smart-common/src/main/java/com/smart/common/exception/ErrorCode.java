package com.smart.common.exception;

import lombok.AllArgsConstructor;
import lombok.Getter;

/**
 * 错误码枚举
 * <p>
 * 集中定义系统中所有的错误码及其对应的错误信息。错误码用于标识具体的错误类型，
 * 便于前端根据错误码进行差异化处理，也便于后端进行问题定位。
 * </p>
 * <p>
 * 错误码分段规则：
 * <ul>
 *   <li>0       —— 成功</li>
 *   <li>10000 段 —— 通用系统级错误（参数校验、数据不存在等）</li>
 *   <li>20000 段 —— 业务实体相关错误（电站、设备、场景等不存在）</li>
 *   <li>30000 段 —— 决策引擎相关错误（决策失败、RAG 检索失败、AI 推理失败等）</li>
 * </ul>
 * 分段设计便于快速识别错误所属模块，也为后续扩展预留了编码空间。
 * </p>
 *
 * @author Joseph Ho
 */
@Getter
@AllArgsConstructor
public enum ErrorCode {

    /**
     * 成功 —— 表示请求处理成功，非错误状态
     */
    SUCCESS(0, "成功"),

    // ==================== 10000 段：通用系统级错误 ====================

    /**
     * 系统异常 —— 未预期的运行时异常，通常需要查看日志排查根因
     */
    SYSTEM_ERROR(10000, "系统异常"),

    /**
     * 参数错误 —— 请求参数校验未通过，如必填项缺失、格式不正确等
     */
    PARAM_ERROR(10001, "参数错误"),

    /**
     * 数据不存在 —— 根据条件查询数据库未找到对应记录
     */
    DATA_NOT_FOUND(10002, "数据不存在"),

    /**
     * 重复操作 —— 同一操作被重复提交，用于接口幂等性保护
     */
    DUPLICATE_OPERATION(10003, "重复操作"),

    // ==================== 20000 段：业务实体相关错误 ====================

    /**
     * 电站不存在 —— 根据电站 ID 未找到对应的电站记录
     */
    STATION_NOT_FOUND(20001, "电站不存在"),

    /**
     * 设备不存在 —— 根据设备 ID 未找到对应的设备记录
     */
    DEVICE_NOT_FOUND(20002, "设备不存在"),

    /**
     * 场景不存在 —— 根据场景 ID 未找到对应的场景配置记录
     */
    SCENE_NOT_FOUND(20003, "场景不存在"),

    // ==================== 30000 段：决策引擎相关错误 ====================

    /**
     * 决策执行失败 —— 决策引擎在执行决策流程时发生异常
     */
    DECISION_FAILED(30001, "决策执行失败"),

    /**
     * RAG 检索失败 —— 从向量知识库检索相关文档时发生异常（如向量数据库不可用）
     */
    RAG_RETRIEVAL_FAILED(30002, "RAG检索失败"),

    /**
     * AI 推理失败 —— 调用大语言模型进行智能推理时发生异常（如模型服务超时）
     */
    AI_INFERENCE_FAILED(30003, "AI推理失败");

    /** 错误码数值，用于 API 响应中标识具体的错误类型 */
    private final int code;

    /** 错误信息描述，用于 API 响应中向调用方提供可读的错误说明 */
    private final String message;
}
